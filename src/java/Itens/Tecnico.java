package Itens;

/**
 *
 * @author LnkConsertos
 */
public class Tecnico {
    
    private String nome;
    private String foto;

    public Tecnico(String nome, String foto) {
        this.nome = nome;
        this.foto = foto;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }
    
    
    
}
